class N
{
	public static void main(String[] args) 
	{
		int i = 0;
		System.out.println(i);
		System.out.println(i ++);	//post way
		System.out.println(i);
		System.out.println("----------");
		i = 0;
		System.out.println(i);
		System.out.println(++ i);	//pre way
		System.out.println(i);
		System.out.println("----------");
	}
}
