class O
{
	public static void main(String[] args) 
	{
		int i = 20;
		switch(i)
		{
			case 20:
				System.out.println("from case20");
				break;
			case 21:
				System.out.println("from case21");
				break;
			case 22:
				System.out.println("from case22");
				break;
			case 24:
				System.out.println("from case22");
				break;
			default:
				System.out.println("from default");
				break;
		}
		System.out.println("Hello World!");
	}
}
